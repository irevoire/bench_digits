#![feature(int_log)]

pub fn log10(i: u32) -> u32 {
    ((i + 1) as f64).log10().ceil() as u32
}

pub fn divide_loop(n: u32) -> u32 {
    std::iter::successors(Some(n), |n| {
        let n = n / 10;
        (n != 0).then(|| n)
    })
    .count() as u32
}

